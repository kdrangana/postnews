function getCurrentLang() {
    return localStorage.getItem('site_lang') || 'si';
}

function setLang(lang) {
    localStorage.setItem('site_lang', lang);
    location.reload();
}

function t(key) {
    const lang = getCurrentLang();
    return translations[lang][key] || translations['si'][key] || key;
}

// Global utility to translate all data-lang elements on the page
function translatePage() {
    const elements = document.querySelectorAll('[data-lang]');
    elements.forEach(el => {
        const key = el.getAttribute('data-lang');
        if (translations[getCurrentLang()][key]) {
            el.innerText = translations[getCurrentLang()][key];
        }
    });
}

document.addEventListener('DOMContentLoaded', translatePage);

// Helper to get translated field from post
function getP(post, field) {
    if (!post) return '';
    const lang = getCurrentLang();
    const specificField = lang === 'si' ? field : `${field}_${lang}`;
    if (post[specificField]) return post[specificField];
    // Fallback to Sinhala or any available
    return post[field] || post[`${field}_en`] || post[`${field}_ta`] || '';
}
